package sec07_loops;

public class vid032_for_loop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println("for loop Test no." + i);
        }

        System.out.println();

        int j = 0;
        while (j < 10) {
            System.out.println("while loop Test no." + j);
            j++;
        }
    }
}
